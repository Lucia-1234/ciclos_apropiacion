//inicia ciclo for con una variable inicializada en 0
//se indica que el ciclo se itera hasta que se deje de cumplir la condicion y el numero sea mayor a 50
for (i = 0; i <= 50; i++) {
  //condicion que verifica los numeros primos y los muestra en consola  
  if (i % 2 == 0) {
    console.log(`los numeros primos entre 1 y 50 son: ${i}`);
  }
}